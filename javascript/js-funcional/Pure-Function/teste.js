function soma(a, b) {
    return a + b
};

function subtracao(c, d) {
    return c - d
};

function multiplicacao(e, f) {
    return e * f
};

function divisao(g, h) {
    return g / h
};

console.log(soma(1, 2))
console.log(subtracao(3, 4))
console.log(multiplicacao(5, 6))
console.log(divisao(7, 8))